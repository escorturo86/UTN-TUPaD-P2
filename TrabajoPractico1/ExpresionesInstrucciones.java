public class ExpresionesInstrucciones {
    // Voy a usar el ejemplo y luego explico
    public static void main(String[] args) {
        int x = 10;            // Línea 1: instrucción de declaración+asignación
        x = x + 5;             // Línea 2: instrucción de asignación; "x + 5" es una expresión
        System.out.println(x); // Línea 3: instrucción (llamada a método)
    }
}

/*
Explicación:
- Una EXPRESIÓN es algo que produce un valor (por ejemplo: x + 5).
- Una INSTRUCCIÓN (o sentencia) es una orden completa que hace algo
  (por ejemplo: declarar una variable, asignar, o llamar a un método).
En el código:
- Línea 1 y 2 y 3 son INSTRUCCIONES.
- Dentro de la línea 2, "x + 5" es una EXPRESIÓN (genera un valor).
*/
