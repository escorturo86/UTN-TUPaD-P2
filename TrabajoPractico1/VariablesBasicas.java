public class VariablesBasicas {
    // Declaro variables con valores y las muestro
    public static void main(String[] args) {
        String nombre = "Alejandro";  // texto
        int edad = 39;                 // número entero
        double altura = 1.87;          // número con decimales
        boolean estudiante = true;     // verdadero/falso

        System.out.println("Nombre: " + nombre);
        System.out.println("Edad: " + edad);
        System.out.println("Altura: " + altura + " m");
        System.out.println("¿Es estudiante? " + estudiante);
    }
}
